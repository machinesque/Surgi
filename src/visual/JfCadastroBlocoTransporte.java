    public void setBlocoBuscado(Bloco blocoBuscado) {
        
        if (blocoBuscado == null) {
            
        } else {
            
            iniciaClasseGeral();
            iniciaBloco();
            
            jcbLote.setSelectedItem("LOTE " + blocoBuscado.getLote().getNumeroLote() + " - " + blocoBuscado.getLote().getNomeLote());
            jtfNomeBloco.setText(blocoBuscado.getNomeBloco());
            jtfNumeroBloco.setText(blocoBuscado.getNumeroBloco());
            jtfTotalKm.setText(classeGeral.pontoPorVirgula(String.valueOf(blocoBuscado.getTotalKm())));
            
            codigo = blocoBuscado.getCodigoBloco();
            bloco = blocoBuscado;
            bloco.setCodigoBloco(codigo);
            
            desabilitaCampos();

            opcao = "alterar";

            abilitaBotoes();
            
        }
        
    }


//Metodos DAO===========================================================================================================================================
    public void salvar() {

        iniciaDao();
        iniciaBloco();
        iniciaClasseGeral();
        iniciaControleLote();
        iniciaLote();
        
        lote = (Lote) dao.busca(lote, controleLote.getCodigoLote(controleLote.getNomeLote(jcbLote.getSelectedItem().toString())));
        bloco.setLote(lote);
        bloco.setNomeBloco(jtfNomeBloco.getText());
        bloco.setNumeroBloco(jtfNumeroBloco.getText());
        bloco.setTotalKm(Double.parseDouble(classeGeral.virgulaPorPonto(jtfTotalKm.getText())));

        dao.inserir(bloco, 7, codigoUsuario);
        cancelar();

    }
    
    public void atualizar() {

        iniciaDao();
        iniciaBloco();
        iniciaClasseGeral();
        iniciaControleLote();
        iniciaLote();

        lote = (Lote) dao.busca(lote, controleLote.getCodigoLote(controleLote.getNomeLote(jcbLote.getSelectedItem().toString())));
        bloco.setLote(lote);
        bloco.setNomeBloco(jtfNomeBloco.getText());
        bloco.setNumeroBloco(jtfNumeroBloco.getText());
        bloco.setTotalKm(Double.parseDouble(classeGeral.virgulaPorPonto(jtfTotalKm.getText())));
        
        dao.atualizar(bloco, 7, codigoUsuario);
        cancelar();

    }

    public void excluir() {

        iniciaClasseGeral();

        if (classeGeral.msgConfirma("Deseja excluir o Bloco?")) {

            iniciaDao();
            iniciaBloco();
            
            dao.excluir(bloco, codigoUsuario);
            cancelar();

        }

    }

