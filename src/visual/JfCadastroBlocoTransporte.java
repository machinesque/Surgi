    public void fechar() {
        if (!jtfNomeBloco.getText().isEmpty() || !jtfNumeroBloco.getText().isEmpty() || !jtfTotalKm.getText().isEmpty()) {
            iniciaClasseGeral();
            classeGeral.msgAtencao("Salve ou cancele o cadastro em edição!");
        } else {
            this.dispose();
        }
        
    }

    public void cancelar() {

        blocoVerificado = null;
        
        jtfNomeBloco.setText("");
        jtfNumeroBloco.setText("");
        jtfTotalKm.setText("");
        
        preencheComboLote();
        
        abilitaCampos();

        desabilitaBotoes();

        opcao = "salvar";
    }

