    public void cancelar() {

        DefaultTableModel modelo = (DefaultTableModel) jtbBuscaCrachas.getModel();

        jDateChooserBuscaData.setDate(null);
        jtfBuscaCargo.setText("");
        jtfBuscaNome.setText("");
        jtfBuscaRepresentacao.setText("");
        
        jDateChooserBuscaData.setDate(null);

        modelo.setRowCount(0);
        
        dao = null;

    }

    public void pesquisar() {

        DefaultTableModel modelo = (DefaultTableModel) jtbBuscaCrachas.getModel();
        final TableRowSorter<TableModel> tabelaSorter = new TableRowSorter<TableModel>(modelo);
        jtbBuscaCrachas.setRowSorter(tabelaSorter);
        
        iniciaDao();
        
        List listaCrachas = new ArrayList();
        Date dataCracha;

        if (jDateChooserBuscaData.getDate() != null) {
            dataCracha = jDateChooserBuscaData.getDate();
        } else {
            dataCracha = null;
        }
        
        listaCrachas = dao.listarCrachasComae(jtfBuscaNome.getText(), jtfBuscaRepresentacao.getText(), jtfBuscaCargo.getText(), dataCracha);
        //listaAgendamento = dao.listarAgendamento(nomeEntid, new java.sql.Date(dataSolic.getTime()), tipoEntid);Usando jFormatedTextField
            
        modelo.setRowCount(0);

        for (int i = 0; i < listaCrachas.size(); i++) {

            iniciaCrachasComae();
            
            crachasComae = (CrachasComae) listaCrachas.get(i);

            modelo.addRow(new String[]{String.valueOf(crachasComae.getCodigoCrachaComae()), crachasComae.getNomeCracha1(), crachasComae.getRepresentacao1(), 
                crachasComae.getCargoCracha1(), crachasComae.getNomeCracha2(), crachasComae.getRepresentacao2(), crachasComae.getCargoCracha2(), 
                crachasComae.getNomeCracha3(), crachasComae.getRepresentacao3(), crachasComae.getCargoCracha3(), crachasComae.getNomeCracha4(),
                crachasComae.getRepresentacao4(), crachasComae.getCargoCracha4()});

            crachasComae = null;
            
        }

        if (jtbBuscaCrachas.getRowCount() > 0) {
            jtbBuscaCrachas.getSelectionModel().setSelectionInterval(0, 0);
        } else {
            iniciaClasseGeral();
            
            classeGeral.msgInforma("Não há crachás com estes dados!");
        }

    }

    public void buscaDados() {

        iniciaDao();
        iniciaCrachasComae();
        //DAO dao = new DAO();

        int codigo;

        codigo = Integer.valueOf((String) jtbBuscaCrachas.getValueAt(jtbBuscaCrachas.getSelectedRow(), 0));

        crachasComae = (CrachasComae) dao.busca(crachasComae, codigo);
        dao.encerraSessao();

    }

