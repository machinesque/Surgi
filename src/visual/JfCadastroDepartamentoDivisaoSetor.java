//Outros Metodos =================================================================================================================================================
    public void fechar() {
        
        if (!jtfNomeDepartamentoDivisaoSetor.getText().isEmpty()) {

            iniciaClasseGeral();
            classeGeral.msgAtencao("Cancele ou Salve o Cadastro em Edição!");

        } else {
            dao = null;
            this.dispose();
        }

    }

    public void cancelar() {

        jtfNomeDepartamentoDivisaoSetor.setText("");
        
        abilitaCampos();
        desabilitaBotoes();

    }

    public void abilitaCampos() {

        jtfNomeDepartamentoDivisaoSetor.setEditable(true);

    }

    public void desabilitaCampos() {

        jtfNomeDepartamentoDivisaoSetor.setEditable(false);

    }

    public void abilitaBotoes() {

        jbCancelar.setEnabled(true);
        jbBuscar.setEnabled(true);
        jbImprimir.setEnabled(true);

        if (cadastrarDepartamentoDivisaoSetor == true) {
            jbSalvar.setEnabled(false);
            jbExcluir.setEnabled(true);
            jbAlterar.setEnabled(true);
        } else {
            jbSalvar.setEnabled(false);
            jbExcluir.setEnabled(false);
            jbAlterar.setEnabled(false);
        }

    }

    public void desabilitaBotoes() {

        jbCancelar.setEnabled(true);
        jbBuscar.setEnabled(true);
        jbImprimir.setEnabled(false);

        if (cadastrarDepartamentoDivisaoSetor == true) {
            jbSalvar.setEnabled(true);
            jbExcluir.setEnabled(false);
            jbAlterar.setEnabled(false);
        } else {
            jbSalvar.setEnabled(false);
            jbExcluir.setEnabled(false);
            jbAlterar.setEnabled(false);
        }

    }

