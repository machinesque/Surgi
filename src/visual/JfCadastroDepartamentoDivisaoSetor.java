//Getters and Setters==============================================================================================================================================
    public void setDepartamentoDivisaoSetor(DepartamentoDivisaoSetor departamentoDivisaoSetorBuscado) {
        
        if (departamentoDivisaoSetorBuscado == null) {
        } else {
            
            iniciaDepartamentoDivisaoSetor();
            
            jtfNomeDepartamentoDivisaoSetor.setText(departamentoDivisaoSetorBuscado.getNomeDepartamentoDivisaoSetor());

            codigo = departamentoDivisaoSetorBuscado.getCodigoDepartamentoDivisaoSetor();
            departamentoDivisaoSetor = departamentoDivisaoSetorBuscado;
            departamentoDivisaoSetor.setCodigoDepartamentoDivisaoSetor(codigo);

            desabilitaCampos();

            abilitaBotoes();

        }
        
    }

    public void setNumeroCaracteres() {
        jtfNomeDepartamentoDivisaoSetor.setDocument(new FixedLengthDocument(120));
    }

    public int getCodigoUsuario() {
        return codigoUsuario;
    }

    public void setCodigoUsuario(int codigoUsuario) {
        this.codigoUsuario = codigoUsuario;
        getPermissoes();
        desabilitaBotoes();
    }

    public void getPermissoes() {
        Permissoes permissoes;
        iniciaDao();

        permissoes = (Permissoes) dao.buscaPermissoes(codigoUsuario);

        cadastrarDepartamentoDivisaoSetor = permissoes.isCadastrarDepartamentoDivisaoSetor();
    }


//Metodos DAO======================================================================================================================================================
    public void salvar() {

        if (!jtfNomeDepartamentoDivisaoSetor.getText().isEmpty()) {

            iniciaDao();
            iniciaDepartamentoDivisaoSetor();

            departamentoDivisaoSetor.setNomeDepartamentoDivisaoSetor(jtfNomeDepartamentoDivisaoSetor.getText());

            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Departamento/Divisão/Setor", "Tentou Salvar " + departamentoDivisaoSetor.getNomeDepartamentoDivisaoSetor());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.inserir(departamentoDivisaoSetor, 7, codigoUsuario);

            cancelar();

        } else {

            iniciaClasseGeral();
            classeGeral.msgAtencao("Complete os Campos Obrigatórios, destacados em Azul!");

        }

    }

    public void atualizar() {

        if (!jtfNomeDepartamentoDivisaoSetor.getText().isEmpty()) {

            iniciaDao();
            iniciaDepartamentoDivisaoSetor();

            departamentoDivisaoSetor.setNomeDepartamentoDivisaoSetor(jtfNomeDepartamentoDivisaoSetor.getText());

            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Departamento/Divisão/Setor", "Tentou Atualizar Código: " + departamentoDivisaoSetor.getCodigoDepartamentoDivisaoSetor() + " do(a) " + departamentoDivisaoSetor.getNomeDepartamentoDivisaoSetor());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.atualizar(departamentoDivisaoSetor, 7, codigoUsuario);

            cancelar();

        } else {
            
            iniciaClasseGeral();
            classeGeral.msgAtencao("Complete os Campos Obrigatórios, destacados em Azul!");
            
        }

    }

    public void excluir() {

        iniciaClasseGeral();
        
        if (classeGeral.msgConfirma("Deseja excluir o Departamento/Divisão/Setor?")) {

            iniciaDao();
            iniciaDepartamentoDivisaoSetor();
            
            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Departamento/Divisão/Setor", "Tentou Excluir Código: " + departamentoDivisaoSetor.getCodigoDepartamentoDivisaoSetor() + " do(a) " + departamentoDivisaoSetor.getNomeDepartamentoDivisaoSetor());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.excluir(departamentoDivisaoSetor, codigoUsuario);
            cancelar();

        }

    }

