    public void excluir() {

        iniciaClasseGeral();

        if (classeGeral.msgConfirma("Deseja excluir o Equipamento ?")) {

            iniciaEquipamento();
            iniciaDao();
            
            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Equipamento", "Tentou Excluir Código: " + equipamento.getCodigoEquipamento() + " " + equipamento.getTipoEquipamento() + " Marca " + equipamento.getMarcaEquipamento() + " Modelo " + equipamento.getModeloEquipamento() + " Patrimônio " + equipamento.getPatrimonioEquipamento());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.excluir(equipamento, codigoUsuario);
            cancelar();

        }

    }
