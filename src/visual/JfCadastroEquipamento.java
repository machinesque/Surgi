    public void salvar() {

        if (!jtfMarca.getText().isEmpty() && !jtfModelo.getText().isEmpty() && !jtfPatrimonio.getText().isEmpty()) {

            iniciaDao();
            iniciaControleTipoEquipamento();
            iniciaEquipamento();
            iniciaTipoEquipamento();

            equipamento.setMarcaEquipamento(jtfMarca.getText());
            equipamento.setModeloEquipamento(jtfModelo.getText());
            equipamento.setCorEquipamento(jtfCor.getText());
            equipamento.setDiponibilidadeEmprestimo(disponibilidadeEmprestimo);
            equipamento.setPatrimonioEquipamento(jtfPatrimonio.getText());
            
            tipoEquipamento = (TipoEquipamento) dao.busca(tipoEquipamento, controleTipoEquipamento.getCodigoEquipamento(jcbTipoEquipamento.getSelectedItem().toString()));
            dao.encerraSessao();
            equipamento.setTipoEquipamento(tipoEquipamento);

            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Equipamento", "Tentou Salvar " + equipamento.getTipoEquipamento() + " Marca " + equipamento.getMarcaEquipamento() + " Modelo " + equipamento.getModeloEquipamento() + " Patrimônio " + equipamento.getPatrimonioEquipamento());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.inserir(equipamento, 7, codigoUsuario);
            cancelar();

        } else {
            iniciaClasseGeral();
            classeGeral.msgAtencao("Complete os Campos Obrigatórios, destacados em Azul!");
        }

    }

