    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed

        if (codigoUsuario == 0) {

            JOptionPane.showMessageDialog(this, "Não foi selecionado nenhum usuário!", "Atenção", JOptionPane.WARNING_MESSAGE, null);

        } else {

            if (btgAgendamento.getSelection() == null || btgAgendamentoMultimidia.getSelection() == null || btgAgendamentoPontoDigital.getSelection() == null 
                    || btgAtaGerarNumero.getSelection() == null || btgAtaVisualizarTodos.getSelection() == null || btgCadastroCartucho.getSelection() == null
                    || btgCadastroCmei.getSelection() == null || btgCadastroEscola.getSelection() == null || btgCadastroEquipamentos.getSelection() == null 
                    || btgCadastroOutros.getSelection() == null || btgCadastroTipoEquipamento.getSelection() == null || btgCadastroUsuario.getSelection() == null 
                    || btgImpressosAgendamentos.getSelection() == null || btgImpressosCmeis.getSelection() == null || btgImpressosEscolas.getSelection() == null 
                    || btgImpressosMultimidias.getSelection() == null || btgImpressosOutros.getSelection() == null || btgImpressosPontoDigital.getSelection() == null 
                    || btgImpressosRecarga.getSelection() == null || btgOficiosCiGerarNumeroGeral.getSelection() == null 
                    || btgOficiosCiVisualizarTodosGeral.getSelection() == null
                    || btgPortariaGerarNumero.getSelection() == null || btgPortariaVisualizarTodos.getSelection() == null || btgRecarga.getSelection() == null 
                    || btgResolucaoGerarNumero.getSelection() == null || btgResolucaoVisualizarTodos.getSelection() == null || btgEditalGerarNumero.getSelection() == null 
                    || btgEditalVisualizarTodos.getSelection() == null || btgLogImpressao.getSelection() == null || btgLogVisualizarTodos.getSelection() == null
                    || btgTransporteAluno.getSelection() == null || btgTransporteBloco.getSelection() == null || btgTrasporteLinha.getSelection() == null 
                    || btgCadastroModeloCartucho.getSelection() == null || btgTransporteLote.getSelection() == null) {

                JOptionPane.showMessageDialog(this, "Preencha os campos de Permissões em todas as abas!", "Atenção", JOptionPane.WARNING_MESSAGE, null);

            } else {

                if (opcao.equals("salvar")) {

                    salvar();
                    opcao = "salvar";

                } else if (opcao.equals("alterar")) {

                    atualizar();
                    opcao = "salvar";

                }

            }

        }

    }//GEN-LAST:event_jbSalvarActionPerformed

