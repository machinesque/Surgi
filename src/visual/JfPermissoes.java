    public void verificaUsuarioCadastrado(int codigoVerificador) {

        iniciaDao();
        iniciaPermissoes();
        Permissoes permiss;

        permiss = (Permissoes) dao.buscaPermissoes(codigoVerificador);

        if (permiss == null || permiss.getUsuario() == null) {
            opcao = "salvar";
        } else {

            permissoes = (Permissoes) dao.buscaPermissoes(codigoVerificador);
            setPermissaoBuscada(permissoes);
            opcao = "alterar";
        }

    }

