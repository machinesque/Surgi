    public void setCartuchoBuscado(Cartucho cartuchoBuscado) {

        if (cartuchoBuscado == null) {
        } else {

            iniciaCartucho();
            iniciaModeloCartucho();
            
            if (cartuchoBuscado.getTipoEntidade().equals("ESCOLA")) {
                
                iniciaEscola();
                jrbEscola.setSelected(true);
                preencheComboEscolas();
                
                escola = (Escola) cartuchoBuscado.getEscola();
                jcbNomeEntidade.setSelectedItem(escola.getNomeEscola());
                
            } else if (cartuchoBuscado.getTipoEntidade().equals("CMEI")) {
                
                iniciaCmei();
                jrbCmei.setSelected(true);
                preencheComboCmeis();
                
                cmei = (Cmei) cartuchoBuscado.getCmei();
                jcbNomeEntidade.setSelectedItem(cmei.getNomeCmei());
                
            } else if (cartuchoBuscado.getTipoEntidade().equals("OUTROS")) {
                
                iniciaOutro();
                jrbOutros.setSelected(true);
                preencheComboOutros();
                
                outro = (Outro) cartuchoBuscado.getOutro();
                jcbNomeEntidade.setSelectedItem(outro.getNomeOutro());
                
            }

            jtfCodigoBarras.setText(cartuchoBuscado.getCodigoBarra());
            
            modeloCartucho = cartuchoBuscado.getModeloCartucho();
            jcbModeloCartucho.setSelectedItem(modeloCartucho.getNomeModeloCartucho());
            
            jcheckbCartuchoQueimado.setSelected(cartuchoBuscado.isCartuchoQueimado());

            codigo = cartuchoBuscado.getCodigoCartucho();
            cartucho = cartuchoBuscado;
            cartucho.setCodigoCartucho(codigo);

            desabilitaCampos();

            abilitaBotoes();

        }

        jcbModeloCartucho.transferFocusBackward();

    }

