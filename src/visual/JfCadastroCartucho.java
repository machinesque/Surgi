    public void salvar() {

        if (!jtfCodigoBarras.getText().isEmpty() && jcbModeloCartucho.getSelectedIndex() !=0 && jcbNomeEntidade.getSelectedIndex() != 0) {

            iniciaDao();
            iniciaCartucho();
            iniciaControleModeloCartucho();
            iniciaModeloCartucho();
            
            cartucho.setCodigoBarra(jtfCodigoBarras.getText());
            
            modeloCartucho = (ModeloCartucho) dao.busca(modeloCartucho, controleModeloCartucho.getCodigoModeloCartucho(jcbModeloCartucho.getSelectedItem().toString()));
            dao.encerraSessao();
            cartucho.setModeloCartucho(modeloCartucho);
            
            cartucho.setCartuchoQueimado(cartuchoQueimado);
            
            if (jrbEscola.isSelected() == true) {
                
                iniciaControleEscola();
                iniciaEscola();
                cartucho.setTipoEntidade("ESCOLA");
                
                escola = (Escola) dao.busca(escola, controleEscola.getCodigoEscola(jcbNomeEntidade.getSelectedItem().toString()));
                dao.encerraSessao();
                cartucho.setEscola(escola);
                
            } else if (jrbCmei.isSelected() == true) {
                
                iniciaCmei();
                iniciaControleCmei();
                cartucho.setTipoEntidade("CMEI");

                cmei = (Cmei) dao.busca(cmei, controleCmei.getCodigoCmei(jcbNomeEntidade.getSelectedItem().toString()));
                dao.encerraSessao();
                cartucho.setCmei(cmei);
                
            } else if (jrbOutros.isSelected() == true) {
                
                iniciaControleOutro();
                iniciaOutro();
                cartucho.setTipoEntidade("OUTROS");
                
                outro = (Outro) dao.busca(outro, controleOutro.getCodigoOutro(jcbNomeEntidade.getSelectedItem().toString()));
                dao.encerraSessao();
                cartucho.setOutro(outro);
                
            }

            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Cartucho", "Tentou Salvar para " + cartucho.getTipoEntidade() + " " + jcbNomeEntidade.getSelectedItem().toString() + " Modelo: " + cartucho.getModeloCartucho());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.inserir(cartucho, 7, codigoUsuario);
            cancelar();

        } else {
            iniciaClasseGeral();
            classeGeral.msgAtencao("Complete os Campos Obrigatórios, destacados em Azul!");
        }

    }

    public void atualizar() {

        if (!jtfCodigoBarras.getText().isEmpty() && jcbModeloCartucho.getSelectedIndex() !=0 && jcbNomeEntidade.getSelectedIndex() != 0) {

            iniciaDao();
            iniciaCartucho();
            iniciaControleModeloCartucho();
            iniciaModeloCartucho();
            
            cartucho.setCodigoBarra(jtfCodigoBarras.getText());
            
            modeloCartucho = (ModeloCartucho) dao.busca(modeloCartucho, controleModeloCartucho.getCodigoModeloCartucho(jcbModeloCartucho.getSelectedItem().toString()));
            dao.encerraSessao();
            cartucho.setModeloCartucho(modeloCartucho);
            
            if (jrbEscola.isSelected() == true) {
                
                iniciaControleEscola();
                iniciaEscola();
                cartucho.setTipoEntidade("ESCOLA");
                
                escola = (Escola) dao.busca(escola, controleEscola.getCodigoEscola(jcbNomeEntidade.getSelectedItem().toString()));
                dao.encerraSessao();
                cartucho.setEscola(escola);
                
            } else if (jrbCmei.isSelected() == true) {
                
                iniciaCmei();
                iniciaControleCmei();
                cartucho.setTipoEntidade("CMEI");

                cmei = (Cmei) dao.busca(cmei, controleCmei.getCodigoCmei(jcbNomeEntidade.getSelectedItem().toString()));
                dao.encerraSessao();
                cartucho.setCmei(cmei);
                
            } else if (jrbOutros.isSelected() == true) {
                
                iniciaControleOutro();
                iniciaOutro();
                cartucho.setTipoEntidade("OUTROS");
                
                outro = (Outro) dao.busca(outro, controleOutro.getCodigoOutro(jcbNomeEntidade.getSelectedItem().toString()));
                dao.encerraSessao();
                cartucho.setOutro(outro);
                
            }

            if (cartucho.isCartuchoQueimado() == false && cartuchoQueimado == true) {
                iniciaEnviaEmail();
                iniciaValidaData();
                enviaEmail.enviarEmailNaoDeuRecarga(cartucho.getTipoEntidade(), jcbNomeEntidade.getSelectedItem().toString(), validaData.getDateAtual(), codigoUsuario, 
                        jcbModeloCartucho.getSelectedItem().toString(), jtfCodigoBarras.getText());
            }
            
            cartucho.setCartuchoQueimado(cartuchoQueimado);
            
            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Cartucho", "Tentou Atualizar Código: " + cartucho.getCodigoCartucho() + " do(a) " + cartucho.getTipoEntidade() + " " + 
                        jcbNomeEntidade.getSelectedItem().toString() + " Modelo: " + cartucho.getModeloCartucho());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.atualizar(cartucho, 7, codigoUsuario);
            cancelar();

        } else {
            iniciaClasseGeral();
            classeGeral.msgAtencao("Complete os Campos Obrigatórios, destacados em Azul!");
        }

    }

    public void excluir() {
        
        if (classeGeral.msgConfirma("Deseja excluir o cartucho ?")) {

            iniciaDao();
            iniciaCartucho();
            
            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Cadastro Cartucho", "Tentou Excluir Código: " + cartucho.getCodigoCartucho() + " do(a) " + cartucho.getTipoEntidade() + " " + jcbNomeEntidade.getSelectedItem().toString() + " Modelo: " + cartucho.getModeloCartucho());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.excluir(cartucho, codigoUsuario);
            cancelar();

        }
    }
