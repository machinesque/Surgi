    public void setEditalBuscado(Edital editalBuscado) {

        if (editalBuscado == null) {
        } else {
            
            iniciaEdital();
            iniciaUsuario();
            
            if (editalBuscado.getModeloEdital().equals("EDITAL")) {
                jrbEdital.setSelected(true);
                preencheNumeroEdital();
            } 

            usuario = (Usuario) editalBuscado.getUsuario();
            jtfUsuario.setText(usuario.getNomeUsuario());
            usuario = null;
            
            jtfNumeroEdital.setText("" + editalBuscado.getNumero());
            jDateChooserSolicitacao.setDate(editalBuscado.getDataSolicitacao());
            jtfAssuntoEdital.setText(editalBuscado.getAssuntoEdital());
            jtfDestinatarioEdital.setText(editalBuscado.getDestinatarioEdital());
            jtfDepartamentoDestinatarioEdital.setText(editalBuscado.getDepartamentoDestinatario());

            codigo = editalBuscado.getCodigoEdital();//para testar comando excluir
            edital = editalBuscado;
            edital.setCodigoEdital(codigo);

            opcao = "alterar";

            desabilitaCampos();

            abilitaBotoes();

        }
    }

