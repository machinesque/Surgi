    public void cancelar() {

        iniciaControleUsuario();
        
        modeloEdital = "EDITAL";
        jtfUsuario.setText(controleUsuario.getNomeUsuario(codigoUsuario));
        jrbEdital.setSelected(true);
        preencheNumeroEdital();
        jDateChooserSolicitacao.setDate(getDateHoje());
        jtfAssuntoEdital.setText("");
        jtfDestinatarioEdital.setText("");
        jtfDepartamentoDestinatarioEdital.setText("");

        abilitaCampos();

        desabilitaBotoes();

        opcao = "salvar";
        codigo = 0;

    }

    public void abilitaCampos() {

        jrbEdital.setEnabled(true);
        jDateChooserSolicitacao.setEnabled(true);
        jtfAssuntoEdital.setEditable(true);
        jtfDestinatarioEdital.setEditable(true);
        jtfDepartamentoDestinatarioEdital.setEditable(true);

    }

    public void desabilitaCampos() {

        jrbEdital.setEnabled(false);
        jDateChooserSolicitacao.setEnabled(false);
        jtfAssuntoEdital.setEditable(false);
        jtfDestinatarioEdital.setEditable(false);
        jtfDepartamentoDestinatarioEdital.setEditable(false);

    }

    public void bloqueiaCampos() {

        jtfUsuario.setEditable(false);
        jtfNumeroEdital.setEditable(false);
    }

    public void abilitaBotoes() {

        jbCancelar.setEnabled(true);
        jbBuscar.setEnabled(true);
        jbImprimir.setEnabled(true);

        if (gerarNumeracao == true) {
            jbSalvar.setEnabled(false);
            jbExcluir.setEnabled(true);
            jbAlterar.setEnabled(true);
        } else {
            jbSalvar.setEnabled(false);
            jbExcluir.setEnabled(false);
            jbAlterar.setEnabled(false);
        }

    }

    public void desabilitaBotoes() {

        jbCancelar.setEnabled(true);
        jbBuscar.setEnabled(true);
        jbImprimir.setEnabled(false);

        if (gerarNumeracao == true) {
            jbSalvar.setEnabled(true);
            jbExcluir.setEnabled(false);
            jbAlterar.setEnabled(false);
        } else {
            jbSalvar.setEnabled(false);
            jbExcluir.setEnabled(false);
            jbAlterar.setEnabled(false);
        }

    }

