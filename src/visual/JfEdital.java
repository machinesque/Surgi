    public void salvar() {

        if (!jtfUsuario.getText().isEmpty() && !jtfNumeroEdital.getText().isEmpty() && jDateChooserSolicitacao.getDate() != null) {

            iniciaDao();
            iniciaEdital();
            iniciaUsuario();

            edital.setAno(versao.getAno());
            edital.setDataSolicitacao(jDateChooserSolicitacao.getDate());
            edital.setNumero(Integer.parseInt(jtfNumeroEdital.getText()));
            
            usuario = (Usuario) dao.buscaUsuario(0, jtfUsuario.getText().toString(), "");
            edital.setUsuario(usuario);
            
            edital.setTipoEdital(TIPOEDITAL);
            edital.setAssuntoEdital(jtfAssuntoEdital.getText());
            edital.setDepartamentoDestinatario(jtfDepartamentoDestinatarioEdital.getText());
            
            edital.setDestinatarioEdital(jtfDestinatarioEdital.getText());
            if (jrbEdital.isSelected() == true) {
                edital.setModeloEdital(modeloEdital);
            } 

            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Edital", "Tentou Salvar Nº: " + jtfNumeroEdital.getText() + " Assunto: " + jtfAssuntoEdital.getText() + " Destinatário: " + jtfDestinatarioEdital.getText());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.inserir(edital, 7, codigoUsuario);
            cancelar();

        } else {
            iniciaClasseGeral();
            classeGeral.msgAtencao("Complete os Campos Obrigatórios, destacados em Azul!");
        }

    }

