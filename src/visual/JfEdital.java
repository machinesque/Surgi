    public void excluir() {

        iniciaClasseGeral();

        if (classeGeral.msgConfirma("Deseja excluir o Edital?")) {

            iniciaDao();
            iniciaEdital();
            
            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Edital", "Tentou Excluir Nº: " + jtfNumeroEdital.getText() + " Assunto: " + jtfAssuntoEdital.getText() + " Destinatário: " + jtfDestinatarioEdital.getText());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.excluir(edital, codigoUsuario);
            cancelar();

        }

    }

