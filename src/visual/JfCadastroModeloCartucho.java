    public void salvar() {

        if (!jtfNomeModeloCartucho.getText().isEmpty()) {

            iniciaDao();
            //DAO dao = new DAO();

            modeloCartucho.setNomeModeloCartucho(jtfNomeModeloCartucho.getText());

            try {
                geraLog.criaLog(codigoUsuario, "Cadastro Modelo Cartucho", "Tentou Salvar " + modeloCartucho.getNomeModeloCartucho());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.inserir(modeloCartucho, 7, codigoUsuario);

            cancelar();

        } else {
            iniciaClasseGeral();
            classeGeral.msgAtencao("Complete os Campos Obrigatórios, destacados em Azul!");
        }

    }

