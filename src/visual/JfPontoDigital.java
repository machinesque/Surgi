    public void setPontoDigitalBuscado(Agendamento pontoDigitalBuscado) {

        if (pontoDigitalBuscado == null) {
        } else {
            
            iniciaUsuario();
            
            if (pontoDigitalBuscado.getTipoEntidade().equals("ESCOLA")) {
                
                jrbEscola.setSelected(true);
                tipoEntid = "ESCOLA";
                preencheComboEscolas();
                jcbNomeEntidade.setSelectedItem(pontoDigitalBuscado.getEscola().getNomeEscola());
                
            } else if (pontoDigitalBuscado.getTipoEntidade().equals("CMEI")) {
                
                jrbCmei.setSelected(true);
                tipoEntid = "CMEI";
                preencheComboCmeis();
                jcbNomeEntidade.setSelectedItem(pontoDigitalBuscado.getCmei().getNomeCmei());
                
            } else if (pontoDigitalBuscado.getTipoEntidade().equals("OUTROS")) {
                
                jrbOutros.setSelected(true);
                tipoEntid = "OUTROS";
                preencheComboOutros();
                jcbNomeEntidade.setSelectedItem(pontoDigitalBuscado.getOutro().getNomeOutro());
                
            }

            preencheComboUsuarios();

            jtaProblemaInformado.setText(pontoDigitalBuscado.getProblemaInformado());
            jtaProblemaConstatado.setText(pontoDigitalBuscado.getProblemaConstatado());
            jtfLocalEntidade.setText(pontoDigitalBuscado.getLocalEntidade());
            jcbStatus.setSelectedItem(pontoDigitalBuscado.getStatus());
            
            jcbNomeUsuario.setSelectedItem(pontoDigitalBuscado.getUsuario());
            if (pontoDigitalBuscado.getUsuarioAtendimento() != null) {
                jcbNomeUsuarioAtendimento.setSelectedItem(pontoDigitalBuscado.getUsuarioAtendimento().getNomeUsuario());
            }
            
            jDateChooserSolicitacao.setDate(pontoDigitalBuscado.getDataSolicitacao());
            jDateChooserResolucao.setDate(pontoDigitalBuscado.getDataResolucao());

            codigo = pontoDigitalBuscado.getCodigoAgendamento();//para testar comando excluir
            agendamento = pontoDigitalBuscado;
            agendamento.setCodigoAgendamento(codigo);

            desabilitaCampos();
            
            abilitaBotoes();

        }
    }

