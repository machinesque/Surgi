    public void fechar() {

        if (jcbNomeEntidade.getSelectedIndex() != 0 || !jtfLocalEntidade.getText().equals("PONTO DIGITAL") || !jtaProblemaInformado.getText().isEmpty() || !jtaProblemaConstatado.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Cancele ou Salve o Agendamento\n do Ponto Digital em Edição!", "Atenção", JOptionPane.WARNING_MESSAGE, null);

        } else {

            dao = null;
            cancelar();
            this.dispose();
        }
    }

    public void cancelar() {
        
        iniciaAgendamento();
        iniciaDao();
        iniciaUsuario();

        agendamento.setAnoAgendamento("");
        agendamento.setDataResolucao(null);
        agendamento.setDataSolicitacao(null);
        agendamento.setDataRecolhimento(null);
        agendamento.setLocalEntidade("");
        agendamento.setUsuario(null);
        agendamento.setProblemaConstatado("");
        agendamento.setProblemaInformado("");
        agendamento.setStatus("");
        agendamento.setTipoAgendamento("");

        jrbEscola.setSelected(true);
        tipoEntid = "ESCOLA";
        preencheComboEscolas();
        jcbNomeEntidade.setSelectedIndex(0);
        jcboxAnotacao.setSelected(false);
        jDateChooserSolicitacao.setDate(getDateHoje());
        jDateChooserResolucao.setDate(null);
        jDateChooserRecolhimento.setDate(null);
        jtaProblemaInformado.setText("");
        jtaProblemaConstatado.setText("");
        jtfLocalEntidade.setText("PONTO DIGITAL");
        tipoAgend = "AGENDAMENTO PONTO DIGITAL";
        jcbStatus.setSelectedIndex(0);

        preencheComboUsuarios();

        abilitaCampos();

        usuario = (Usuario) dao.busca(usuario, codigoUsuario);
        dao.encerraSessao();
        jcbNomeUsuario.setSelectedItem(usuario.getNomeUsuario());
        jcbNomeUsuarioAtendimento.setSelectedItem(usuario.getNomeUsuario());
        
        cmei = null;
        controleCmei = null;
        controleEscola = null;
        controleOutro = null;
        escola = null;
        outro = null;
        usuario = null;

        desabilitaBotoes();

        opcao = "salvar";
        codigo = 0;

    }

