    public void setInstrucaoNormativaBuscada(InstrucaoNormativa instrucaoNormativaBuscada) {

        if (instrucaoNormativaBuscada == null) {
        } else {

            iniciaInstrucaoNormativa();
            iniciaUsuario();
            
            if (instrucaoNormativaBuscada.getModeloInstrucao().equals("INSTRUÇÃO NORMATIVA")) {
                jrbInstrucao.setSelected(true);
                preencheNumeroInstrucao();
            } 

            usuario = (Usuario) instrucaoNormativaBuscada.getUsuario();
            
            jtfUsuario.setText(usuario.getNomeUsuario());
            jtfNumeroInstrucao.setText("" + instrucaoNormativaBuscada.getNumero());
            jDateChooserSolicitacao.setDate(instrucaoNormativaBuscada.getDataSolicitacao());
            jtfAssuntoInstrucao.setText(instrucaoNormativaBuscada.getAssuntoInstrucao());
            jtfDestinatarioInstrucao.setText(instrucaoNormativaBuscada.getDestinatarioInstrucao());
            jtfDepartamentoDestinatarioInstrucao.setText(instrucaoNormativaBuscada.getDepartamentoDestinatario());

            codigo = instrucaoNormativaBuscada.getCodigoInstrucao();//para testar comando excluir
            instrucaoNormativa = instrucaoNormativaBuscada;
            instrucaoNormativa.setCodigoInstrucao(codigo);
                    
            usuario = null;
            
            opcao = "alterar";

            desabilitaCampos();

            abilitaBotoes();

        }
    }

