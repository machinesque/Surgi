    public void excluir() {

        ClasseGeral cg = new ClasseGeral();

        if (cg.msgConfirma("Deseja excluir a Instrução Normativa?")) {

            iniciaDao();
            iniciaInstrucaoNormativa();
            
            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Instrução Normativa", "Tentou Excluir Nº: " + jtfNumeroInstrucao.getText() + " Assunto: " + jtfAssuntoInstrucao.getText() + " Destinatário: " + jtfDestinatarioInstrucao.getText());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.excluir(instrucaoNormativa, codigoUsuario);
            cancelar();

        }

    }
    
