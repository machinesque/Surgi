        jtfUsuario.setText(controleUsuario.getNomeUsuario(codigoUsuario));
        jrbInstrucao.setSelected(true);
        preencheNumeroInstrucao();
        jDateChooserSolicitacao.setDate(validaData.getDateAtual());
        jtfAssuntoInstrucao.setText("");
        jtfDestinatarioInstrucao.setText("");

