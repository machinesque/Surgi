    public void salvar() {

        if (!jtfUsuario.getText().isEmpty() && !jtfNumeroInstrucao.getText().isEmpty() && jDateChooserSolicitacao.getDate() != null) {

            iniciaDao();
            iniciaInstrucaoNormativa();

            instrucaoNormativa.setAno(versao.getAno());
            instrucaoNormativa.setDataSolicitacao(jDateChooserSolicitacao.getDate());
            instrucaoNormativa.setNumero(Integer.parseInt(jtfNumeroInstrucao.getText()));
            
            usuario = (Usuario) dao.buscaUsuario(0, jtfUsuario.getText().toString(), "");
            instrucaoNormativa.setUsuario(usuario);
            usuario = null;
            
            instrucaoNormativa.setTextoInstrucao("");
            instrucaoNormativa.setTipoInstrucao(TIPOINSTRUCAO);
            instrucaoNormativa.setAssuntoInstrucao(jtfAssuntoInstrucao.getText());
            instrucaoNormativa.setDepartamentoDestinatario(jtfDepartamentoDestinatarioInstrucao.getText());
            
            instrucaoNormativa.setDestinatarioInstrucao(jtfDestinatarioInstrucao.getText());
            if (jrbInstrucao.isSelected() == true) {
                instrucaoNormativa.setModeloInstrucao(modeloInstrucao);
            } 

            try {
                iniciaLog();
                geraLog.criaLog(codigoUsuario, "Instrução Normativa", "Tentou Salvar Nº: " + jtfNumeroInstrucao.getText() + " Assunto: " + jtfAssuntoInstrucao.getText() + " Destinatário: " + jtfDestinatarioInstrucao.getText());
            } catch (IOException ex) {
                Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            dao.inserir(instrucaoNormativa, 7, codigoUsuario);
            cancelar();

        } else {
            JOptionPane.showMessageDialog(null, "Complete os Campos Obrigatórios, destacados em Azul!", "Atenção", JOptionPane.WARNING_MESSAGE, null);
        }

    }

