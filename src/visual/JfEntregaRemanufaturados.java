    public void salvar() {

        iniciaDao();
        iniciaEntregaRemanufaturados();
        iniciaUsuario();
        iniciaValidaData();
        
        usuario = (Usuario) dao.busca(usuario, codigoUsuario);

        entregaRemanufaturados.setAno(versao.getAno());
        
        if (jDateChooserDataRetorno.getDate() == null) {
            jDateChooserDataRetorno.setDate(validaData.getDateAtual());
        }
        
        entregaRemanufaturados.setDataRetorno(jDateChooserDataRetorno.getDate());
        entregaRemanufaturados.setNumeroEntregaRemanufaturados(Integer.valueOf(jtfEntregaNumero.getText()));
        entregaRemanufaturados.setProtocoloFinalizado(true);
        entregaRemanufaturados.setUsuario(usuario);
        
        try {
            iniciaLog();
            geraLog.criaLog(codigoUsuario, "Protocolo de Entrega de Remanufaturados", "Tentou Salvar Nº " + entregaRemanufaturados.getNumeroEntregaRemanufaturados());
        } catch (IOException ex) {
            Logger.getLogger(JfPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        dao.inserir(entregaRemanufaturados, 7, codigoUsuario);

    }

