    public void preencheCartuchosNaTabela(EntregaRemanufaturados tabelaSolicitacao) {
        
        DefaultTableModel modelo = (DefaultTableModel) jtbEntregaCartuchos.getModel();
        
        modelo.setRowCount(0);
        
        iniciaDao();
        
        List listaEntregaRemanufaturadosItensItens = new ArrayList();
        
        listaEntregaRemanufaturadosItensItens = dao.listarEntregaRemanufaturadosItens(tabelaSolicitacao.getCodigoEntregaRemanufaturados(), 0);
        
        for (int i = 0; i < listaEntregaRemanufaturadosItensItens.size(); i++) {

            iniciaCartucho();
            iniciaEntregaRemanufaturadosItens();
            String nomeUnidade = "";
            
            entregaRemanufaturadosItens = (EntregaRemanufaturadosItens) listaEntregaRemanufaturadosItensItens.get(i);
            
            cartucho = (Cartucho) dao.busca(cartucho, entregaRemanufaturadosItens.getCartucho().getCodigoCartucho());
            
            switch (cartucho.getTipoEntidade()) {
                
                case "CMEI":
                    nomeUnidade = cartucho.getCmei().getNomeCmei();
                    break;
                    
                case "ESCOLA":
                    nomeUnidade = cartucho.getEscola().getNomeEscola();
                    break;
                    
                case "OUTROS":
                    nomeUnidade = cartucho.getOutro().getNomeOutro();
                    break;
                    
            }
            
            modelo.addRow(new String[]{String.valueOf(i + 1), cartucho.getTipoEntidade(), nomeUnidade, cartucho.getModeloCartucho().getNomeModeloCartucho(), cartucho.getCodigoBarra()});

            entregaRemanufaturadosItens = null;
            cartucho = null;
        }
        
    }
    
