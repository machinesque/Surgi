    private void jbFinalizarProtocoloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFinalizarProtocoloActionPerformed

        if (jtfEntregaNumero.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Preencha os campos obrigatórios destacados em azul!", "Atenção", JOptionPane.WARNING_MESSAGE, null);

        } else {

            if (opcao.equals("salvar")) {

                verificaNumeroProtocoloEntrega();
                
                if (!entregaRemanufaturadosVerificado.isEmpty()) {

                    iniciaClasseGeral();

                    if (classeGeral.msgConfirmaOpcao("Já existe um Protocolo Cadastrastrado!\n Deseja Salvar/Criar um novo Protocolo? \n Clique em 'NÃO' para buscar e complementar o Protocolo já existente! \n Clique em 'SIM' para Salvar/Criar o novo Protocolo!")) {
                        salvar();
                        opcao = "salvar";
                    }

                } else {
                    salvar();
                    opcao = "salvar";
                }

            } else if (opcao.equals("alterar")) {

                atualizar();
                opcao = "salvar";

            }

            jbFinalizarProtocolo.setEnabled(false);
            jbImprimir.setEnabled(true);
            
        }
    }//GEN-LAST:event_jbFinalizarProtocoloActionPerformed

