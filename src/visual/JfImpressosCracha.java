    public void salvar() {
        
        iniciaDao();
        iniciaCrachas();
       
        crachas.setCargoCracha1(jtfCargoCracha.getText());
        crachas.setCargoCracha2(jtfCargoCracha2.getText());
        crachas.setCargoCracha3(jtfCargoCracha3.getText());
        crachas.setCargoCracha4(jtfCargoCracha4.getText());
        crachas.setDataCracha(jDateChooserDataCracha.getDate());
        crachas.setDataEntrega(jDateChooserDataEntrega.getDate());
        crachas.setDepartamentoCracha1(jtfDepartamentoCracha.getText());
        crachas.setDepartamentoCracha2(jtfDepartamentoCracha2.getText());
        crachas.setDepartamentoCracha3(jtfDepartamentoCracha3.getText());
        crachas.setDepartamentoCracha4(jtfDepartamentoCracha4.getText());
        
        if (jcbImpresso.getSelectedIndex() == 0) {
            crachas.setImpresso(false);
        } else {
            crachas.setImpresso(true);
        }
        
        crachas.setNomeCracha1(jtfNomeCracha.getText());
        crachas.setNomeCracha2(jtfNomeCracha2.getText());
        crachas.setNomeCracha3(jtfNomeCracha3.getText());
        crachas.setNomeCracha4(jtfNomeCracha4.getText());
        
        dao.inserir(crachas, 7, codigoUsuario);
        cancelar();
        
    }
    
