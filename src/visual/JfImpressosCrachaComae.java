    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        
        if (((!jtfCargoCracha.getText().isEmpty() || !jtfRepresentacaoCracha.getText().isEmpty()) && !jtfNomeCracha.getText().isEmpty()) ||
                ((!jtfCargoCracha2.getText().isEmpty() || !jtfRepresentacaoCracha2.getText().isEmpty()) && !jtfNomeCracha2.getText().isEmpty()) ||
                ((!jtfCargoCracha3.getText().isEmpty() || !jtfRepresentacaoCracha3.getText().isEmpty()) && !jtfNomeCracha3.getText().isEmpty()) ||
                ((!jtfCargoCracha4.getText().isEmpty() || !jtfRepresentacaoCracha4.getText().isEmpty()) && !jtfNomeCracha4.getText().isEmpty())
                ) {
            
            if (opcao.equals("salvar")) {
                salvar();
                opcao = "salvar";
            } else {
                atualizar();
                opcao = "salvar";
            }
            
        } else {
            
            iniciaClasseGeral();
            classeGeral.msgAtencao("Preencha os campos de Nome, e Cargo ou Departamento, para impressão do crachá!");
            
        }
        
    }//GEN-LAST:event_jbSalvarActionPerformed

