    public void salvar() {
        
        iniciaDao();
        iniciaCrachasComae();
       
        crachasComae.setCargoCracha1(jtfCargoCracha.getText());
        crachasComae.setCargoCracha2(jtfCargoCracha2.getText());
        crachasComae.setCargoCracha3(jtfCargoCracha3.getText());
        crachasComae.setCargoCracha4(jtfCargoCracha4.getText());
        crachasComae.setDataCracha(jDateChooserDataCracha.getDate());
        crachasComae.setDataEntrega(jDateChooserDataEntrega.getDate());
        crachasComae.setRepresentacao1(jtfRepresentacaoCracha.getText());
        crachasComae.setRepresentacao2(jtfRepresentacaoCracha2.getText());
        crachasComae.setRepresentacao3(jtfRepresentacaoCracha3.getText());
        crachasComae.setRepresentacao4(jtfRepresentacaoCracha4.getText());
        
        if (jcbImpresso.getSelectedIndex() == 0) {
            crachasComae.setImpresso(false);
        } else {
            crachasComae.setImpresso(true);
        }
        
        crachasComae.setNomeCracha1(jtfNomeCracha.getText());
        crachasComae.setNomeCracha2(jtfNomeCracha2.getText());
        crachasComae.setNomeCracha3(jtfNomeCracha3.getText());
        crachasComae.setNomeCracha4(jtfNomeCracha4.getText());
        
        dao.inserir(crachasComae, 7, codigoUsuario);
        cancelar();
        
    }
    
