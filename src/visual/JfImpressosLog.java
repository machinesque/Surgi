    public void cancelar() {

        iniciaDao();
        iniciaTabela();
        iniciaUsuario();

        jDateChooserDataAcao.setDate(null);
        
        usuario = (Usuario) dao.busca(usuario, codigoUsuario);
        dao.encerraSessao();
        jcbNomeUsuarioLog.setSelectedItem(usuario.getNomeUsuario());
        usuario = null;

        modeloTabela.setRowCount(0);

    }

    public void pesquisar() {

        iniciaDao();
        iniciaControleUsuario();
        iniciaTabela();
        
        Log2 log2;
        List listaLog = new ArrayList();
        int codigoUser;
        Date dataAcao;

        codigoUser = controleUsuario.getCodigoUsuario(jcbNomeUsuarioLog.getSelectedItem().toString());
        
        if (jDateChooserDataAcao.getDate() == null) {
            listaLog = dao.listarLogs(codigoUser, null);
        } else {
            //try {
                //dataSolic = df.parse(jftfBuscaDataSolicitacao.getText()); Usando jFormatedTextField
            //} catch (ParseException ex) {
            //    Logger.getLogger(jdBuscaAgendamento.class.getName()).log(Level.SEVERE, null, ex);
            //}
            listaLog = dao.listarLogs(codigoUser, jDateChooserDataAcao.getDate());
        }

        //listaAgendamento = dao.listarAgendamento(nomeEntid, new java.sql.Date(dataSolic.getTime()), tipoEntid);Usando jFormatedTextField
            
        modeloTabela.setRowCount(0);


        for (int i = 0; i < listaLog.size(); i++) {

            iniciaUsuario();
            log2 = (Log2) listaLog.get(i);
            
            usuario = (Usuario) log2.getUsuario();

            modeloTabela.addRow(new String[]{String.valueOf(log2.getCodigoLog2()), usuario.getNomeUsuario(), String.valueOf(log2.getTelaJanela()), String.valueOf(log2.getAcao()), String.valueOf(log2.getDataAcao())});

        }

        if (jtbLog.getRowCount() > 0) {
            jtbLog.getSelectionModel().setSelectionInterval(0, 0);
        }

    }

    public void buscaDados() {

        iniciaDao();

        int codigo;

        codigo = Integer.valueOf((String) jtbLog.getValueAt(jtbLog.getSelectedRow(), 0));

        log = (Log2) dao.busca(log, codigo);
        dao.encerraSessao();

    }

    public void desabilitaCampos() {

        if (visualizarLog) {
            jcbNomeUsuarioLog.setEditable(true);
            jDateChooserDataAcao.setEnabled(true);
        } else {
            jcbNomeUsuarioLog.setEditable(false);
            jDateChooserDataAcao.setEnabled(false);
        }
    }

    
    
