    public void setCodigoUsuario(int codigoUsuario) {
        iniciaDao();
        iniciaUsuario();
        
        usuario = (Usuario) dao.busca(usuario, codigoUsuario);
        dao.encerraSessao();
        jcbNomeUsuarioLog.setSelectedItem(usuario.getNomeUsuario());
        usuario = null;
        
        this.codigoUsuario = codigoUsuario;
        getPermissoes();
        desabilitaCampos();
        desabilitaBotoes();
    }

    public void getPermissoes() {
        Permissoes permissoes;
        iniciaDao();
        //DAO dao = new DAO();

        permissoes = (Permissoes) dao.buscaPermissoes(codigoUsuario);
        
        visualizarLog = permissoes.isVisualizarLog();
        impressosLog = permissoes.isImpressosLog();
    }

